/**
 * This code contains functions for fetching, creating, and updating notifications, as well as removing notifications.
 * It also includes error handling for when these functions fail.
 */
import { createSlice, createAsyncThunk } from "@reduxjs/toolkit";
import { setMessage } from "../site/message";

import NotificationService from "services/notification/notification.service";

export const get = createAsyncThunk(
    "notification/get",
    async ({ id }, thunkAPI) => {
        try {
            const response = await NotificationService.get(id);
            thunkAPI.dispatch(setMessage(response.data.message));
            return response.data;
        } catch (error) {
            const message =
                (error.response &&
                    error.response.data &&
                    error.response.data.message) ||
                error.message ||
                error.toString();
            thunkAPI.dispatch(setMessage(message));
            return thunkAPI.rejectWithValue();
        }
    }
);

