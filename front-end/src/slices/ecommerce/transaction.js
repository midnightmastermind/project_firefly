/**
 * This code contains functions for fetching, creating, and updating transactions, as well as removing transactions.
 * It also includes error handling for when these functions fail.
 */
import { createSlice, createAsyncThunk } from "@reduxjs/toolkit";
import { setMessage } from "../site/message";

import TransactionService from "services/ecommerce/transaction.service";

export const get = createAsyncThunk(
    "transaction/get",
    async ({ id }, thunkAPI) => {
        try {
            const response = await TransactionService.get(id);
            thunkAPI.dispatch(setMessage(response.data.message));
            return response.data;
        } catch (error) {
            const message =
                (error.response &&
                    error.response.data &&
                    error.response.data.message) ||
                error.message ||
                error.toString();
            thunkAPI.dispatch(setMessage(message));
            return thunkAPI.rejectWithValue();
        }
    }
);

