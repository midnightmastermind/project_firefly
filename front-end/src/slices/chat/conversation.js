/**
 * This code contains functions for fetching, creating, and updating conversations, as well as removing conversations.
 * It also includes error handling for when these functions fail.
 */
import { createSlice, createAsyncThunk } from "@reduxjs/toolkit";
import { setMessage } from "../site/message";

import ConversationService from "services/chat/conversation.service";

export const get = createAsyncThunk(
    "conversation/get",
    async ({ id }, thunkAPI) => {
        try {
            const response = await ConversationService.get(id);
            thunkAPI.dispatch(setMessage(response.data.message));
            return response.data;
        } catch (error) {
            const message =
                (error.response &&
                    error.response.data &&
                    error.response.data.message) ||
                error.message ||
                error.toString();
            thunkAPI.dispatch(setMessage(message));
            return thunkAPI.rejectWithValue();
        }
    }
);

