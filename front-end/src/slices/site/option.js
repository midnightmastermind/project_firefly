/**
 * This code contains functions for fetching, creating, and updating options, as well as removing options.
 * It also includes error handling for when these functions fail.
 */
import { createSlice, createAsyncThunk } from "@reduxjs/toolkit";
import { setMessage } from "./message";

import OptionService from "../services/option.service";

export const get = createAsyncThunk(
    "option/get",
    async ({ id }, thunkAPI) => {
        try {
            const response = await OptionService.get(id);
            thunkAPI.dispatch(setMessage(response.data.message));
            return response.data;
        } catch (error) {
            const message =
                (error.response &&
                    error.response.data &&
                    error.response.data.message) ||
                error.message ||
                error.toString();
            thunkAPI.dispatch(setMessage(message));
            return thunkAPI.rejectWithValue();
        }
    }
);

